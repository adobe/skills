/*
 * Example ExtensionRegistration for a Workfront App Builder extension.
 * Companion to the `workfront-ui-extension` skill. Copy the shape, not verbatim.
 *
 * KEY TRAP: `extensionId` must be NON-EMPTY (set it in Constants.js), and
 * `id: extensionId` must be kept INSIDE `methods` — otherwise the item registers
 * (getItems is even called) but silently never renders. See the skill's Gotchas.
 *
 * Every extension-point `url` must map to a <Route> in App.js.
 */
import React from 'react'
import { register } from '@adobe/uix-guest'
import { extensionId } from './Constants' // must be a non-empty slug, e.g. 'my-wf-ext'

function ExtensionRegistration () {
  const init = async () => {
    await register({
      id: extensionId,          // top-level id, non-empty
      metadata,                 // from app-metadata.json (generated by a build hook)
      methods: {
        id: extensionId,        // REQUIRED here too — Workfront's host consumes methods.id

        // Main Menu button
        mainMenu: {
          getItems () {
            return [{ id: 'my-main-menu', url: '/index.html#/', label: 'My App', icon: 'icon1' }]
          }
        },

        // Left panel, registered per Workfront object type (PROJECT/TASK/ISSUE/PORTFOLIO/PROGRAM)
        secondaryNav: {
          PROJECT: {
            getItems () {
              return [{ id: 'my-project-panel', label: 'My Panel', icon: 'icon2', url: '/index.html#/project-panel' }]
            }
          }
        },

        // Widget embedded in a custom-form field (dimensions optional)
        widgets: {
          getItems () {
            return [{
              id: 'my-widget',
              url: '/index.html#/widget',
              label: 'My Widget',
              dimensions: { height: 400, width: 600 } // maxHeight/maxWidth also allowed
            }]
          }
        }
      }
    })
  }
  init().catch(console.error)
  return <span>IFrame for integration with Host (Workfront)...</span>
}

export default ExtensionRegistration
